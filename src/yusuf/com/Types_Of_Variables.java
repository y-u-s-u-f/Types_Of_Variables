package yusuf.com;

public class Types_Of_Variables {
	
	public static void main(String[] args) {
	
	  System.out.println("Example byte value : " + Byte.MAX_VALUE);
	  System.out.println("Example short value : " + Short.MAX_VALUE);
	  System.out.println("Example integer value : " + Integer.MAX_VALUE);
	  System.out.println("Example long value : " + Long.MAX_VALUE);
	  System.out.println("Example float value : " + Float.MAX_VALUE);
	  System.out.println("Example double value : " + Double.MAX_VALUE);
	  System.out.println("Example char value : " + "g");
	  System.out.println("Example boolean value : " + true);

   }
}
